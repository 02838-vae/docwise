import streamlit as st
from docx import Document
from docx.enum.text import WD_COLOR_INDEX
import re

def is_all_caps(text):
    """Ki·ªÉm tra text c√≥ ph·∫£i to√†n ch·ªØ hoa (v√† kho·∫£ng tr·∫Øng/s·ªë)"""
    cleaned = text.replace(" ", "").replace(".", "")
    return cleaned.isupper() and len(cleaned) > 2

def is_question(text):
    """Nh·∫≠n di·ªán c√¢u h·ªèi"""
    if text.endswith("?"):
        return True
    if "choose the correct group of words" in text.lower():
        return True
    # C√¢u b·∫Øt ƒë·∫ßu b·∫±ng s·ªë v√† c√≥ ch·ªØ
    if re.match(r'^\d+\.', text) and not re.search(r'\d+\s*(m|kg|ft|nm)', text.lower()):
        return True
    return False

def load_questions(file_path):
    doc = Document(file_path)
    sections = {}
    current_section = None
    current_q = None

    for para in doc.paragraphs:
        text = para.text.strip()
        if not text:
            continue

        # N·∫øu l√† ti√™u ƒë·ªÅ ph·ª• l·ª•c
        if text.lower().startswith("ph·ª• l·ª•c"):
            if current_q and current_section:
                sections[current_section].append(current_q)
                current_q = None
            current_section = text
            if current_section not in sections:
                sections[current_section] = []
            continue

        # N·∫øu l√† c√¢u h·ªèi
        if is_question(text):
            if current_q and current_section:
                sections[current_section].append(current_q)
            current_q = {"question": text, "options": []}
            continue

        # N·∫øu l√† ƒë√°p √°n r√µ r√†ng (A., B., C., D., E.)
        if current_q and re.match(r'^[A-E]\.', text):
            is_correct = any(
                run.font.highlight_color == WD_COLOR_INDEX.YELLOW
                for run in para.runs
            )
            current_q["options"].append({"text": text, "correct": is_correct})
            continue

        # N·∫øu l√† ƒë√°p √°n ki·ªÉu in hoa ho·∫∑c m√¥ t·∫£ sau c√¢u h·ªèi
        if current_q and (is_all_caps(text) or not is_question(text)):
            is_correct = any(
                run.font.highlight_color == WD_COLOR_INDEX.YELLOW
                for run in para.runs
            )
            current_q["options"].append({"text": text, "correct": is_correct})
            continue

    if current_q and current_section:
        sections[current_section].append(current_q)

    return sections


def main():
    st.title("üìò B√†i ki·ªÉm tra tr·∫Øc nghi·ªám ti·∫øng Anh k·ªπ thu·∫≠t")

    sections = load_questions("docwise.docx")
    section_names = [s for s in sections.keys() if s.lower().startswith("ph·ª• l·ª•c")]

    chosen_section = st.selectbox("üëâ B·∫°n mu·ªën l√†m ph·∫ßn n√†o?", [""] + section_names)

    if not chosen_section:
        st.info("H√£y ch·ªçn m·ªôt ph·ª• l·ª•c ƒë·ªÉ b·∫Øt ƒë·∫ßu.")
        return

    questions = sections[chosen_section]
    if not questions:
        st.warning(f"‚ùå Kh√¥ng c√≥ c√¢u h·ªèi n√†o trong {chosen_section}")
        return

    st.write(f"üîé ƒêang l√†m: **{chosen_section}** ({len(questions)} c√¢u h·ªèi)")

    # Form quiz
    with st.form("quiz_form"):
        answers = {}
        for i, q in enumerate(questions, start=1):  # ƒë√°nh s·ªë l·∫°i t·ª´ 1
            st.subheader(f"C√¢u {i}: {q['question']}")
            options = [opt["text"] for opt in q["options"]]
            if not options:
                st.warning(f"C√¢u {i} ch∆∞a c√≥ ƒë√°p √°n, b·ªè qua.")
                continue
            answers[i] = st.radio(
                "Ch·ªçn ƒë√°p √°n:",
                options,
                index=None,
                key=f"{chosen_section}_q{i}"
            )

        submitted = st.form_submit_button("‚úÖ N·ªôp b√†i")

    if submitted:
        score = 0
        results = []

        for i, q in enumerate(questions, start=1):
            opts = q["options"]
            if not opts or i not in answers:
                continue
            correct_options = [opt["text"] for opt in opts if opt["correct"]]
            if not correct_options:
                continue
            correct_ans = correct_options[0]
            user_ans = answers[i]

            if user_ans == correct_ans:
                score += 1
                results.append((i, True, user_ans, correct_ans))
            else:
                results.append((i, False, user_ans, correct_ans))

        st.success(f"üéØ K·∫øt qu·∫£: {score}/{len(questions)} c√¢u ƒë√∫ng")

        st.subheader("üìã Chi ti·∫øt k·∫øt qu·∫£:")
        for q_num, is_correct, user_ans, correct_ans in results:
            if is_correct:
                st.write(f"‚úÖ C√¢u {q_num}: ƒê√∫ng ({user_ans})")
            else:
                st.write(f"‚ùå C√¢u {q_num}: Sai. B·∫°n ch·ªçn: {user_ans}. ƒê√°p √°n ƒë√∫ng: {correct_ans}")


if __name__ == "__main__":
    main()
